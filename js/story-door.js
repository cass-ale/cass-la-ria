  if (!doorCanvas) return;

  parseLuminance();
  renderDoor(doorCanvas);

  /* Re-render on theme change (CSS variable transitions) */
  var lastBg = getCSSColor('--color-bg', '');
  var lastWeather = getCSSColor('--color-weather', '');

  function checkThemeChange() {
    var bg = getCSSColor('--color-bg', '');
    var weather = getCSSColor('--color-weather', '');
    if (bg !== lastBg || weather !== lastWeather) {
      lastBg = bg;
      lastWeather = weather;
      renderDoor(doorCanvas);
    }
    requestAnimationFrame(checkThemeChange);
  }
  requestAnimationFrame(checkThemeChange);

