  /* ---- Luminance data (parsed once) ---- */
  var LUM = [];

  function hexToLum(hex) {
    var r = parseInt(hex.slice(1, 3), 16);
    var g = parseInt(hex.slice(3, 5), 16);
    var b = parseInt(hex.slice(5, 7), 16);
    return (0.299 * r + 0.587 * g + 0.114 * b) / 255;
  }

  function parseLuminance() {
    LUM = [];
    for (var r = 0; r < ART.length; r++) {
      var row = ART[r];
      var lumRow = [];
      for (var c = 0; c < row.length; c++) {
        var cell = row[c];
        if (!cell) { lumRow.push(0); continue; }
        var fgL = cell[0] ? hexToLum(cell[0]) : -1;
        var bgL = cell[1] ? hexToLum(cell[1]) : -1;
        lumRow.push([fgL, bgL]);
      }
      LUM.push(lumRow);
    }
  }

  /* ---- Theme color helpers ---- */

  function parseHexToRGB(hex) {
    return [
      parseInt(hex.slice(1, 3), 16),
      parseInt(hex.slice(3, 5), 16),
      parseInt(hex.slice(5, 7), 16)
    ];
  }

  function getCSSColor(prop, fallback) {
    var v = getComputedStyle(document.documentElement)
      .getPropertyValue(prop).trim();
    return v || fallback;
  }

  function buildGradientStops(colorMap) {
    var entries = [];
    for (var i = 0; i < colorMap.length; i++) {
      var c = colorMap[i];
      var lum = (0.299 * c[0] + 0.587 * c[1] + 0.114 * c[2]) / 255;
      entries.push({ color: c, lum: lum });
    }
    entries.sort(function (a, b) { return a.lum - b.lum; });
    var minL = entries[0].lum;
    var maxL = entries[entries.length - 1].lum;
    var range = maxL - minL || 1;
    var stops = [];
    for (var i = 0; i < entries.length; i++) {
      stops.push({
        position: (entries[i].lum - minL) / range,
        color: entries[i].color
      });
    }
    return stops;
  }

  function sampleGradient(stops, t) {
    t = Math.max(0, Math.min(1, t));
    if (t <= stops[0].position) return stops[0].color;
    if (t >= stops[stops.length - 1].position) return stops[stops.length - 1].color;
    for (var i = 0; i < stops.length - 1; i++) {
      if (t >= stops[i].position && t <= stops[i + 1].position) {
        var span = stops[i + 1].position - stops[i].position;
        var frac = span > 0 ? (t - stops[i].position) / span : 0;
        var a = stops[i].color;
        var b = stops[i + 1].color;
        return [
          Math.round(a[0] + (b[0] - a[0]) * frac),
          Math.round(a[1] + (b[1] - a[1]) * frac),
          Math.round(a[2] + (b[2] - a[2]) * frac)
        ];
      }
    }
    return stops[stops.length - 1].color;
  }

  function lumToThemeColor(lum, gradientStops) {
    var rgb = sampleGradient(gradientStops, lum);
    return 'rgb(' + rgb[0] + ',' + rgb[1] + ',' + rgb[2] + ')';
  }

  /* ---- Render the door onto a canvas ---- */

  function renderDoor(canvas) {
    var colorProps = [
      ['--color-bg',           '#f2d4d7'],
      ['--color-bg-subtle',    '#e8c4c8'],
      ['--color-text',         '#2a1f2d'],
      ['--color-text-muted',   '#5c4560'],
      ['--color-accent',       '#8b3a62'],
      ['--color-accent-soft',  '#a34d78'],
      ['--color-border',       '#c9a0ad'],
      ['--color-border-light', '#d4b3be'],
      ['--color-weather',      '#c8b0c0']
    ];
    var themeColors = [];
    for (var ci = 0; ci < colorProps.length; ci++) {
      themeColors.push(parseHexToRGB(
        getCSSColor(colorProps[ci][0], colorProps[ci][1])
      ));
    }
    var gradientStops = buildGradientStops(themeColors);

    /* Size the canvas to fill its CSS container */
    var container = canvas.parentElement;
    var maxW = container.clientWidth;
    var artAspect = COLS / (ROWS * 2);
    var doorH = maxW / artAspect;
    var doorW = maxW;

    /* High-DPI support */
    var dpr = window.devicePixelRatio || 1;
    canvas.width = Math.ceil(doorW * dpr);
    canvas.height = Math.ceil(doorH * dpr);
    canvas.style.width = doorW + 'px';
    canvas.style.height = doorH + 'px';

    var ctx = canvas.getContext('2d');
    ctx.scale(dpr, dpr);

    var cellW = doorW / COLS;
    var cellH = doorH / ROWS;
    var halfH = cellH / 2;

    for (var row = 0; row < ROWS; row++) {
      var lumRow = LUM[row];
      if (!lumRow) continue;
      for (var col = 0; col < COLS; col++) {
        var cell = lumRow[col];
        if (!cell) continue;

        var x = Math.floor(col * cellW);
        var y = Math.floor(row * cellH);
        var w = Math.ceil(cellW) + 1;

        /* Background = top half */
        if (cell[1] >= 0) {
          ctx.fillStyle = lumToThemeColor(cell[1], gradientStops);
          ctx.fillRect(x, y, w, Math.ceil(halfH) + 1);
        }

        /* Foreground = bottom half */
        if (cell[0] >= 0) {
          ctx.fillStyle = lumToThemeColor(cell[0], gradientStops);
          ctx.fillRect(x, y + Math.floor(halfH), w, Math.ceil(halfH) + 1);
        }
      }
    }
  }

